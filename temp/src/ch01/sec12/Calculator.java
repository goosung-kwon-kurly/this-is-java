package ch01.sec12;

public class Calculator {
    public static void main(String[] args) {
        int x = 1;                  //변수 x 선언, 1 저장
        int y = 2;                  //변수 y 선언, 2 저장
        int result = x + y;         //변수 result 선언, x + y 수행 결과 저장
        System.out.println(result); //변수 result 의 값을 화면에 출력
    }
}
